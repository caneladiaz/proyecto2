    private void tblRegistroMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRegistroMousePressed
        //para llevar los paises e índice a la tabla frmDepto
        if (evt.getClickCount() == 2) {//si hace doble click
            int fila = tblRegistro.getSelectedRow();//guarda el índice de la fila
            String cod, cod2, cod3, cod4;
            cod = tblRegistro.getValueAt(fila, 0).toString();//lo del tbl es un objeto
            cod2 = tblRegistro.getValueAt(fila, 6).toString();
            cod3 = tblRegistro.getValueAt(fila, 3).toString();
            cod4 = tblRegistro.getValueAt(fila, 2).toString();

            if (frm_facturaVenta.txtIdAgendamiento != null && frm_facturaVenta.txtIdServicio != null
                    && frm_facturaVenta.txtIdEmpleado != null && frm_facturaVenta.txtidCliente != null) {
                frm_facturaVenta.txtIdAgendamiento.setText(cod);
                frm_facturaVenta.txtIdServicio.setText(cod2);
                frm_facturaVenta.txtIdEmpleado.setText(cod3);
                frm_facturaVenta.txtidCliente.setText(cod4);
            }
            dispose();
        }

    }//GEN-LAST:event_tblRegistroMousePressed

